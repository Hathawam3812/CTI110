print ("Distance Traveled")

# Define Inputs
Speed = int(input("What is the Vehicle Speed in MPH? "))
Hours = int(input("What is the Travel Time in Hours? "))


# Loop function
for i in range(1, Hours+1):
    Distance = Speed * i
    print (i, "     ", Distance)



    
        
